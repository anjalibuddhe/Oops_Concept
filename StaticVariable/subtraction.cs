using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace StaticVariable
{
    internal class Subtraction
    {
        public int SubMethod(int a,int b)
        {
            return a + b;
        }
        public double SubMethod(int a, int b, double c)
        {
            return a * b - c;
        }
        public float SubMethod(float x, int y)
        {
            return x * y;
        }
    }
}
