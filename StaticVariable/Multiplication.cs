using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace StaticVariable
{
    internal class Multiplication
    {
        public int MultiMethod(int a, int b)
        {
            return a * b;
        }
        public float MultiMethod(int a, int b, float c)
        {
            return a * b * c;
        }
        public double MultiMethod(double x, double y)
        {
            return x + y;
        }
    }
}
